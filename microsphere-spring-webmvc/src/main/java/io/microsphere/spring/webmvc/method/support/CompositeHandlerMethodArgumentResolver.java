package io.microsphere.spring.webmvc.method.support;

import org.springframework.core.MethodParameter;
import org.springframework.web.bind.support.WebDataBinderFactory;
import org.springframework.web.context.request.NativeWebRequest;
import org.springframework.web.method.support.HandlerMethodArgumentResolver;
import org.springframework.web.method.support.HandlerMethodArgumentResolverComposite;
import org.springframework.web.method.support.ModelAndViewContainer;

import java.util.List;

/**
 * Composite {@link HandlerMethodArgumentResolver}
 *
 * @author <a href="mailto:mercyblitz@gmail.com">Mercy</a>
 * @see HandlerMethodArgumentResolver
 * @see HandlerMethodArgumentResolverComposite
 * @since 1.0.0
 */
public class CompositeHandlerMethodArgumentResolver implements HandlerMethodArgumentResolver {

    private final List<HandlerMethodArgumentResolver> resolvers;

    public CompositeHandlerMethodArgumentResolver(List<HandlerMethodArgumentResolver> resolvers) {
        this.resolvers = resolvers;
    }


    @Override
    public boolean supportsParameter(MethodParameter parameter) {
        return false;
    }

    @Override
    public Object resolveArgument(MethodParameter parameter, ModelAndViewContainer mavContainer, NativeWebRequest webRequest, WebDataBinderFactory binderFactory) throws Exception {
        return null;
    }
}
